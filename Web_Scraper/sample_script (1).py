def clean_text(text):
    """Removes excess spaces and line breaks."""
    if not text:
        return ""
    text = re.sub(r"\s+", " ", str(text))
    return text.strip()

def normalise_campaign_url(url):
    """Takes the Kickstarter base URL."""
    url = url.split("?")[0].rstrip("/")
    suffixes = ["/description", "/rewards", "/creator", "/faqs", "/posts", "/comments", "/community"]
    for suffix in suffixes:
        if url.endswith(suffix):
            url = url[:-len(suffix)]
    return url

