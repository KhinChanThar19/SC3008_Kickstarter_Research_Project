# Content Extractors
def get_meta_content(soup, attribute_name):
    if soup is None: return ""
    tag = soup.find("meta", property=attribute_name)
    if tag and tag.get("content"):
        return clean_text(tag.get("content"))
    return ""

def remove_unwanted_tags(soup):
    if soup is None: return None
    for tag in soup.find_all(["script", "style", "noscript", "svg", "header", "footer", "nav", "form"]):
        tag.decompose()
    return soup

def extract_visible_text(soup):
    if soup is None: return ""
    soup_copy = BeautifulSoup(str(soup), "html.parser")
    soup_copy = remove_unwanted_tags(soup_copy)

    main_content = soup_copy.find("main")
    if main_content:
        return clean_text(main_content.get_text(" ", strip=True))

    body = soup_copy.find("body")
    if body:
        return clean_text(body.get_text(" ", strip=True))
    return ""

def extract_image_urls(soup):
    if soup is None: return []
    image_urls = []
    for img in soup.find_all("img"):
        image_url = img.get("src") or img.get("data-src")
        if image_url and image_url not in image_urls:
            image_urls.append(image_url)
    return image_urls

def extract_links_containing(soup, keyword):
    if soup is None: return []
    links = []
    for anchor in soup.find_all("a", href=True):
        href = anchor["href"]
        if keyword in href and href not in links:
            links.append(href)
    return links

